class india():
    def capital(self):
        print("New Dehli is the capital of India")

    def language(self):
        print("Hindi is the most widely spoken language of India")

    def type(self):
        print("India is a developing country")

class USA():
    def capital(self):
        print("Washington DC is the capital of the USA")

    def language(self):
        print("English is the most widely spoken language of India")

    def type(self):
        print("The USA is a developed country")

obj1=india()
obj2=USA()

obj1.capital()
obj1.language()
obj1.type()
obj2.capital()
obj2.language()
obj2.type()

